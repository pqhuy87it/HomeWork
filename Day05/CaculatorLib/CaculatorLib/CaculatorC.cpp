#include "CaculatorC.h"
#include <math.h>

int nhan2so(int a,int b)
{
    return a*b;
}

int chia2so(int a,int b)
{
    return a/b;
}

int cong2so(int a,int b)
{
    return a+b;
}

int tru2so(int a,int b)
{
    return a-b;
}

int binhphuong(int x)
{
    return x*x;
}

float khaican(float x)
{
    return pow(x, 0.5);
}
